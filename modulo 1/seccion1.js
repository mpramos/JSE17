//! # Módulo 1 - Introducción a JavaScript

//* ## 1.1.1 Introducción
//? JavaScript es un lenguaje de programación esencial en el desarrollo web.
//? Aprenderlo te permitirá crear aplicaciones interactivas, lo que puede abrirte nuevas oportunidades en el campo de TI.

//* ## 1.1.2 Comunicación con la Computadora
//? Las computadoras están en todas partes y necesitan instrucciones precisas para realizar tareas. Existen dos tipos de lenguajes:
//? - **Lenguajes de máquina**: Son difíciles de leer y escribir para los humanos.
//? - **Lenguajes de programación**: Facilitan la comunicación con la computadora y pueden tener diferentes niveles de abstracción.

//* ## 1.1.3 JavaScript como Lenguaje Interpretado
//? JavaScript es un lenguaje interpretado que se ejecuta principalmente en los navegadores mediante un motor integrado. 
//? También puede ejecutarse en servidores con **Node.js**, lo que amplía su utilidad para el desarrollo backend.

//* ### Compilación JIT
//? Los motores modernos de JavaScript utilizan la **compilación Just-In-Time (JIT)** para mejorar el rendimiento al ejecutar fragmentos de
//? código en tiempo real.

//* ## 1.1.4 Historia de JavaScript
//? JavaScript fue creado en 1995 por **Brendan Eich** para Netscape Navigator bajo el nombre de LiveScript, pero fue renombrado poco después.
// ? Su propósito inicial era hacer que las páginas web fueran dinámicas.

//* ## 1.1.5 Programación del Lado del Cliente vs. Lado del Servidor
// - **Lado del Cliente**: Código JavaScript ejecutado en el navegador para mejorar la interactividad.
// - **Lado del Servidor**: JavaScript ejecutado en servidores con Node.js para manejar lógica backend.

// Hoy en día, JavaScript se usa en **frameworks** como **React** y **Angular**, permitiendo la creación de aplicaciones complejas.

//* ## 1.1.6 Desventajas de JavaScript
// A pesar de su popularidad, JavaScript tiene limitaciones:
// - No es ideal para cálculos avanzados o tareas de alto rendimiento.
// - Ejecutándose en el navegador, su acceso a archivos locales está restringido.
// - Su código es accesible en el navegador, lo que facilita su copia o modificación.

//* ## 1.1.7 Ventajas de JavaScript
// - Comunidad activa y en crecimiento.
// - Gran cantidad de **frameworks y bibliotecas**.
// - Sintaxis sencilla, ideal para principiantes.
// - No requiere herramientas costosas; se puede programar directamente en un navegador.
// - Soporte de empresas como Google y Facebook.

//* ### Tipado Dinámico
// Permite que las variables cambien de tipo en tiempo de ejecución. Aunque flexible, 
// puede generar errores. Para manejar esto, surgió **TypeScript**, una versión tipada de JavaScript.

//* ## 1.1.8 Preparándonos para Programar
// JavaScript se usa en diferentes entornos, pero su **núcleo** es el mismo en todos ellos. 
// Para aprender a programar en este lenguaje, es importante:
// - Aprender **variables, funciones, estructuras de control y bucles**.
// - Escribir mucho código y practicar constantemente.
// - No rendirse ante la frustración; la persistencia es clave.

//* ## 1.2.1 Herramientas de Desarrollo
// Para programar en JavaScript necesitamos:
// - **Editor de código**: Facilita la escritura y comprensión del código.
// - **Intérprete o compilador**: Permite ejecutar el código.
// - **Depurador**: Ayuda a encontrar y corregir errores.

// Podemos trabajar en **entornos locales** o **en línea**. El entorno local ofrece mayor personalización, mientras que los entornos en línea son más accesibles sin instalación.

// ## 1.2.2 Entorno de Desarrollo en Línea
// Existen herramientas en línea como:
// - **JSFiddle**
// - **CodePen**
// - **JsBin**
// - **Plunker**

// Estas plataformas permiten probar código rápidamente sin necesidad de configurar un entorno de desarrollo.

//* ## 1.2.3 Entorno de Desarrollo Local
// El desarrollo local requiere herramientas adicionales:
// - **Gestores de paquetes**: npm, yarn.
// - **Automatizadores de tareas**: Grunt, Webpack.
// - **Frameworks de pruebas**: Mocha, Jest.
// - **Analizadores de seguridad**: Snyk, OWASP Dependency Check.

//* ## 1.2.4 Editor de Código
// Algunos de los editores más populares son:
// - **Visual Studio Code** (Windows, macOS, Linux)
// - **WebStorm** (Entorno comercial con licencia educativa gratuita)
// - **Sublime Text** (Ligero y rápido, con versión de prueba)
// - **Notepad++** (Alternativa gratuita y ligera para Windows)

//* ## 1.2.5 Intérprete de JavaScript
// Para ejecutar código JavaScript, podemos usar:
// - **Navegadores web**: Chrome, Firefox, Edge.
// - **Node.js**: Para desarrollo del lado del servidor.

//* Es recomendable usar la última versión del navegador para compatibilidad con las características modernas de JavaScript.

// todo Ahora que conoces las herramientas necesarias, ¡es momento de empezar a programar!
